const dictionaries = { en, fr }

export function getLocale(): Locale {
  const stored = localStorage.getItem('locale')
  return stored === 'fr' ? 'fr' : 'en'
}

export function setLocale(locale: Locale) {
  localStorage.setItem('locale', locale)
}

export function t(key: string, locale: Locale = getLocale()): string {
  return dictionaries[locale][key] || dictionaries.en[key] || key
}

export function statusLabelKey(status: string): string {
  const map: Record<string, string> = {
    submitted: 'statusSubmitted',
    registry_reviewed: 'statusRegistryReviewed',
    department_approved: 'statusDepartmentApproved',
    admitted: 'statusAdmitted',
    accepted: 'statusAccepted',
    tuition_paid: 'statusTuitionPaid',
    enrolled: 'statusEnrolled',
    rejected: 'statusRejected',
  }
  return map[status] || status
}
